											<h3>L'histoire du club</h3>
											<p>
												Le club d'escalade de Réguiny est situé dans le centre Morbihan, à environ une vingtaine de kilomètres de Pontivy et de Locminé. Il a été créé en 1996, sous l'impulsion de Christophe Jousselin.
												<br>
												Ce club a pour but de promouvoir l'escalade aussi bien sur structures artificielles d'escalade (SAB) qu'en site naturel. Nous nous engageons à vous inculquer les différentes techniques de sécurité ainsi que toute la gestuelle technique nécessaire afin de vous permettre d'être autonome dans la pratique de ce sport. 
												<br>
												Nous disposons d'une SAE allant de 9 à 11m de hauteur, mais également d'un espace bloc de 95 m² et de 4m de hauteur.
												<br>
												Le club est affilié à la fédération française de la montagne et de l'escalade (FFME) et agrée par le ministère de la jeunesse et des sports.
												<br>
												En 2010 nous avons obtenu 2 labels FFME: label escalade et école d'escalade, gage de qualité et de dynamisme du club, en 2013, la FFME nous a renouvellé les 2 labels.
												<br>
												Le club organise aussi des sorties en falaise ou sur bloc (ex: Fontainebleau,Pen Hir, Inzinzac lochrist, l'île aux pies, Pyrénées, et..., le site le plus proche se situe dans les côtes d'Amor à la prénessaye, il est actuellement en cours d'équipement.
												<br>
												Les sorties en extérieur sont prévues à l'avance, vous trouverez donc les dates sur le calendrier du site ou bien sur le tableau d'affichage à la salle, toutefois ces dates sont en fonction des conditions météorologiques.
											</p>
